import React from 'react';
import { format } from 'date-fns';
import { Avatar, AvatarFallback, AvatarImage } from '@/components/ui/avatar';
import { Loader2, UserPlus, Repeat } from 'lucide-react';
import { cn } from '@/lib/utils';

export interface ShiftBlockProps {
  event: {
    id: string;
    title: string;
    start: Date;
    end: Date;
    resource?: {
      booking?: any;
      status?: 'draft' | 'invited' | 'confirmed' | 'pending' | 'completed' | 'past' | 'unassigned';
      type?: 'job' | 'shift';
      isAutoGenerated?: boolean;
    };
  };
  onClick?: () => void;
  isRecurring?: boolean;
}

/**
 * ShiftBlock Component
 * Renders a shift event block with different states:
 * - DRAFT: Ghost slot with "+ Add Staff" label
 * - INVITED: Orange/Amber pill with invitee info and pending spinner
 * - CONFIRMED: Green block with full staff profile
 */
export function ShiftBlock({ event, onClick, isRecurring }: ShiftBlockProps) {
  const status = event.resource?.status || 'draft';
  const shift = event.resource?.booking?.shift || event.resource?.booking?.job;
  const assignedStaff = shift?.assignedStaff || shift?.professional;
  const showRecurring = isRecurring || shift?.isRecurring || shift?.recurringSeriesId;
  const isAutoGenerated = event.resource?.isAutoGenerated;
  const isUnassigned = status === 'unassigned' && isAutoGenerated;

  // State A: UNASSIGNED (Auto-generated ghost slot) - Enhanced ghost style
  if (isUnassigned) {
    return (
      <div
        onClick={onClick}
        data-testid="shift-block-unassigned-slot"
        className={cn(
          "w-full h-full rounded border-2 border-dashed",
          "bg-blue-50/10 dark:bg-blue-950/20 cursor-pointer",
          "flex items-center justify-center",
          "hover:bg-blue-50/30 dark:hover:bg-blue-950/30 transition-all",
          "text-blue-600 dark:text-blue-400 relative p-2"
        )}
        style={{ 
          minHeight: '24px',
          borderColor: 'rgba(59, 130, 246, 0.5)',
        }}
      >
        <div className="flex items-center gap-1.5">
          <UserPlus className="h-4 w-4" />
          <span className="text-sm font-semibold">{event.title || 'Unassigned'}</span>
        </div>
        {showRecurring && (
          <Repeat className="h-3 w-3 absolute top-1 right-1 text-blue-500" />
        )}
      </div>
    );
  }

  // State B: DRAFT (Ghost Slot) - Enhanced ghost style
  if (status === 'draft') {
    return (
      <div
        onClick={onClick}
        data-testid="shift-block-ghost-slot"
        className={cn(
          "w-full h-full rounded border-2 border-dashed",
          "bg-transparent cursor-pointer",
          "flex items-center justify-center",
          "hover:bg-blue-50/50 dark:hover:bg-blue-950/20 transition-all",
          "text-blue-600 dark:text-blue-400 relative p-2"
        )}
        style={{ 
          minHeight: '24px',
          borderColor: 'rgba(59, 130, 246, 0.5)',
        }}
      >
        <div className="flex items-center gap-1.5">
          <UserPlus className="h-4 w-4" />
          <span className="text-sm font-semibold">Add Staff</span>
        </div>
        {showRecurring && (
          <Repeat className="h-3 w-3 absolute top-1 right-1 text-blue-500" />
        )}
      </div>
    );
  }

  // State C: INVITED (Pending)
  if (status === 'invited') {
    const staffName = assignedStaff?.name || assignedStaff?.displayName || 'Staff Member';
    const initials = staffName
      .split(' ')
      .map((n: string) => n[0])
      .join('')
      .toUpperCase()
      .slice(0, 2);

    return (
      <div
        onClick={onClick}
        data-testid="shift-block-pending"
        className={cn(
          "w-full h-full rounded-full px-2 py-1",
          "bg-amber-500 border-2 border-amber-600",
          "cursor-pointer hover:bg-amber-600 transition-colors",
          "flex items-center gap-2 relative"
        )}
        style={{ minHeight: '24px' }}
      >
        <Avatar className="h-5 w-5 border border-amber-700">
          <AvatarImage src={assignedStaff?.photoURL || assignedStaff?.avatar} />
          <AvatarFallback className="bg-amber-600 text-white text-xs">
            {initials}
          </AvatarFallback>
        </Avatar>
        <span className="text-white text-xs font-medium truncate flex-1">
          {staffName}
        </span>
        <Loader2 className="h-3 w-3 text-white animate-spin flex-shrink-0" />
        {showRecurring && (
          <Repeat className="h-3 w-3 text-white absolute top-0 right-0" />
        )}
      </div>
    );
  }

  // State D: CONFIRMED (Locked) - Green with full profile and drop shadow
  if (status === 'confirmed' || status === 'filled') {
    const staffName = assignedStaff?.name || assignedStaff?.displayName || 'Staff Member';
    const initials = staffName
      .split(' ')
      .map((n: string) => n[0])
      .join('')
      .toUpperCase()
      .slice(0, 2);

    return (
      <div
        onClick={onClick}
        data-testid="shift-block-confirmed"
        className={cn(
          "w-full h-full rounded p-2",
          "bg-green-600 border-2 border-green-700",
          "cursor-pointer hover:bg-green-700 transition-colors",
          "flex items-center gap-2 relative"
        )}
        style={{ 
          minHeight: '24px',
          boxShadow: '0 2px 4px rgba(0, 0, 0, 0.15)',
        }}
      >
        <Avatar className="h-5 w-5 border border-green-800">
          <AvatarImage src={assignedStaff?.photoURL || assignedStaff?.avatar} />
          <AvatarFallback className="bg-green-700 text-white text-xs">
            {initials}
          </AvatarFallback>
        </Avatar>
        <div className="flex-1 min-w-0">
          <div className="text-white text-sm font-semibold truncate">
            {staffName}
          </div>
          {event.title && (
            <div className="text-green-100 text-xs font-medium truncate">
              {event.title}
            </div>
          )}
        </div>
        {showRecurring && (
          <Repeat className="h-3 w-3 text-white absolute top-1 right-1" />
        )}
      </div>
    );
  }

  // Fallback: Default rendering for other statuses
  return (
    <div
      onClick={onClick}
      className={cn(
        "w-full h-full rounded px-2 py-1",
        "bg-blue-600 border-2 border-blue-700",
        "cursor-pointer hover:bg-blue-700 transition-colors",
        "text-white text-xs font-medium relative"
      )}
      style={{ minHeight: '24px' }}
    >
      {event.title || 'Shift'}
      {showRecurring && (
        <Repeat className="h-3 w-3 text-white absolute top-0 right-0" />
      )}
    </div>
  );
}

